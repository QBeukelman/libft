#include "../includes/libft.h"

bool	ft_isspace(char c)
{
	if (c == ' '
		|| c == '\t'
		|| c == '\n'
		|| c == '\r'
		|| c == '\f'
		|| c == '\v')
		return (true);
	return (false);
}
